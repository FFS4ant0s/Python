"""
Operadores em python 
+,-,/,//,**,%,()
"""

print(10*10) #Multiplicacao
print(10+10) #Adicão
print(10-5) #subtracão
print(10/3) #Divisão
print(10//3) #Divisão arrendondada
print(2**10) #Expodenciacão
print(10%4) #Modulo
print((5+2)*10) #Precedência
print(20*'F') #Repeticão
print(10 ** (1/2)) #Raiz quadrada